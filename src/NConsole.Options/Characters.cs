namespace NConsole.Options
{
    /// <summary>
    /// Provides several useful <see cref="char"/> based constant assets.
    /// </summary>
    internal static class Characters
    {
        /// <summary>
        /// &apos;,&apos;
        /// </summary>
        public const char Comma = ',';

        /// <summary>
        /// &apos;{&apos;
        /// </summary>
        public const char CurlyBracesOpen = '{';

        /// <summary>
        /// &apos;}&apos;
        /// </summary>
        public const char CurlyBracesClose = '}';

        /// <summary>
        /// &apos;&lt;&apos;
        /// </summary>
        public const char AngleBracketOpen = '<';

        /// <summary>
        /// &apos;&gt;&apos;
        /// </summary>
        public const char AngleBracketClose = '>';

        /// <summary>
        /// &apos;[&apos;
        /// </summary>
        public const char SquareBracketOpen = '[';

        /// <summary>
        /// &apos;]&apos;
        /// </summary>
        public const char SquareBracketClose = ']';

        /// <summary>
        /// &apos;=&apos;
        /// </summary>
        public const char Equal = '=';

        /// <summary>
        /// &apos;:&apos;
        /// </summary>
        public const char Colon = ':';

        /// <summary>
        /// &apos;|&apos;
        /// </summary>
        public const char Pipe = '|';

        /// <summary>
        /// &apos;\0&apos;
        /// </summary>
        public const char ZedChar = '\0';

        /// <summary>
        /// &apos;-&apos;
        /// </summary>
        public const char Dash = '-';

        /// <summary>
        /// &apos;/&apos;
        /// </summary>
        public const char Slash = '/';

        /// <summary>
        /// &apos;+&apos;
        /// </summary>
        public const char Plus = '+';

        /// <summary>
        /// &apos; &apos;
        /// </summary>
        public const char Space = ' ';

        /// <summary>
        /// &apos;\n&apos;
        /// </summary>
        public const char NewLine = '\n';

        /// <summary>
        /// &apos;.&apos;
        /// </summary>
        public const char Dot = '.';
    }
}
